#!/bin/bash
# ==============================================================================
# entrypoint.sh — Скрипт запуска контейнера Scanmatik-Web
# ==============================================================================
# Порядок запуска КРИТИЧЕСКИ ВАЖЕН: каждый следующий компонент
# зависит от предыдущего. Нарушение порядка приведёт к ошибкам.
# ==============================================================================

set -e

echo "============================================="
echo "  Scanmatik-Web Container Starting..."
echo "============================================="

# --------------------------------------------------------------------------
# Шаг 1: Запуск Xvfb (виртуальный X11-дисплей)
# --------------------------------------------------------------------------
# Создаём виртуальный монитор 1024x768 с 16-бит цветом.
# Без этого шага ни Wine, ни VNC не смогут работать —
# им нужен X-сервер для отрисовки окон.
echo "[1/7] Запуск Xvfb на дисплее :0 (1024x768x16)..."
Xvfb :0 -screen 0 1024x768x16 &
XVFB_PID=$!
sleep 2

# Проверяем, что Xvfb запустился
if ! kill -0 $XVFB_PID 2>/dev/null; then
    echo "ОШИБКА: Xvfb не запустился!"
    exit 1
fi
echo "       Xvfb запущен (PID: $XVFB_PID)"

# --------------------------------------------------------------------------
# Шаг 2: Запуск оконного менеджера Fluxbox
# --------------------------------------------------------------------------
# Fluxbox управляет окнами внутри виртуального дисплея:
# заголовки, перемещение, ресайз. Без него окно Wine будет
# отображаться без рамки и его нельзя будет двигать.
echo "[2/7] Запуск оконного менеджера Fluxbox..."
fluxbox &
FLUXBOX_PID=$!
sleep 1
echo "       Fluxbox запущен (PID: $FLUXBOX_PID)"

# --------------------------------------------------------------------------
# Шаг 3: Запуск VNC-сервера (x11vnc)
# --------------------------------------------------------------------------
# x11vnc захватывает содержимое виртуального дисплея :0
# и транслирует его по протоколу VNC (порт 5900).
# -forever  — не завершаться после первого дисконнекта
# -nopw     — без пароля (для упрощения, в проде добавить -passwd)
# -shared   — разрешить несколько одновременных подключений
# -rfbport  — порт VNC-сервера
echo "[3/7] Запуск x11vnc (VNC-сервер, порт 5900)..."
x11vnc -display :0 -forever -nopw -shared -rfbport 5900 &
X11VNC_PID=$!
sleep 1
echo "       x11vnc запущен (PID: $X11VNC_PID)"

# --------------------------------------------------------------------------
# Шаг 4: Запуск websockify + noVNC (веб-интерфейс)
# --------------------------------------------------------------------------
# websockify проксирует WebSocket-соединения из браузера (порт 8080)
# в обычный TCP на VNC-сервер (порт 5900).
# noVNC — это HTML5 VNC-клиент, встроенный в веб-страницу.
# После запуска интерфейс доступен по адресу:
#   http://<IP сервера>:8080/vnc.html
echo "[4/7] Запуск noVNC + websockify (порт 8080 → VNC:5900)..."

# Определяем путь к noVNC (может отличаться в разных дистрибутивах)
NOVNC_DIR="/usr/share/novnc"
if [ ! -d "$NOVNC_DIR" ]; then
    NOVNC_DIR="/usr/share/novnc/utils/../"
fi

websockify --web=$NOVNC_DIR 8080 localhost:5900 &
WEBSOCKIFY_PID=$!
sleep 1
echo "       noVNC доступен на http://0.0.0.0:8080/vnc.html (PID: $WEBSOCKIFY_PID)"

# --------------------------------------------------------------------------
# Шаг 5: Запуск socat (TCP → виртуальный COM-порт)
# --------------------------------------------------------------------------
# socat слушает TCP порт 9000 и создаёт псевдотерминал /dev/ttyV0.
# Каждое входящее TCP-соединение (от Android-шлюза) перенаправляется
# в этот псевдотерминал как поток сырых байтов.
#
# reuseaddr — позволяет повторно биндить порт после реконнекта
# fork      — обработка каждого соединения в отдельном процессе
# pty       — создать псевдотерминал
# link=     — символическая ссылка на удобное имя /dev/ttyV0
# raw       — сырой режим (без обработки спецсимволов)
# echo=0    — не дублировать входящие данные обратно
echo "[5/7] Запуск socat (TCP:9000 → /dev/ttyV0)..."
socat TCP-LISTEN:9000,reuseaddr,fork PTY,link=/dev/ttyV0,raw,echo=0 &
SOCAT_PID=$!
sleep 2

# Ждём появления /dev/ttyV0
for i in $(seq 1 10); do
    if [ -e /dev/ttyV0 ]; then
        break
    fi
    sleep 0.5
done

if [ ! -e /dev/ttyV0 ]; then
    echo "ПРЕДУПРЕЖДЕНИЕ: /dev/ttyV0 ещё не создан (socat ждёт первое TCP-подключение)"
else
    echo "       /dev/ttyV0 создан"
fi
echo "       socat слушает порт 9000 (PID: $SOCAT_PID)"

# --------------------------------------------------------------------------
# Шаг 6: Маппинг COM-порта в Wine
# --------------------------------------------------------------------------
# Wine использует директорию dosdevices для маппинга Linux-устройств
# на Windows COM-порты. Символическая ссылка com1 → /dev/ttyV0
# заставит Сканматик 2 видеть наш TCP-поток как обычный COM1.
echo "[6/7] Маппинг /dev/ttyV0 → Wine COM1..."
mkdir -p /root/.wine/dosdevices

# Удаляем старую ссылку если есть
rm -f /root/.wine/dosdevices/com1

# Создаём символическую ссылку
ln -s /dev/ttyV0 /root/.wine/dosdevices/com1
echo "       COM1 → /dev/ttyV0 (символическая ссылка создана)"

# --------------------------------------------------------------------------
# Шаг 7: Запуск Сканматик 2 через Wine
# --------------------------------------------------------------------------
# Запускаем Windows-EXE через Wine. Путь /app/Scanmatik.exe — условный,
# пользователь подменит его на реальный бинарник через volume mount.
echo "[7/7] Запуск Scanmatik.exe через Wine..."

SCANMATIK_EXE="/app/smwin.exe"

if [ -f "$SCANMATIK_EXE" ]; then
    wine "$SCANMATIK_EXE" &
    WINE_PID=$!
    echo "       Wine запущен с $SCANMATIK_EXE (PID: $WINE_PID)"
else
    echo "       ПРЕДУПРЕЖДЕНИЕ: $SCANMATIK_EXE не найден!"
    echo "       Подключите EXE через volume: -v /path/to/Scanmatik.exe:/app/Scanmatik.exe"
    echo "       Контейнер продолжает работать — noVNC и COM-порт доступны."
fi

echo ""
echo "============================================="
echo "  Scanmatik-Web запущен успешно!"
echo "  noVNC:  http://0.0.0.0:8080/vnc.html"
echo "  TCP:    порт 9000 (для Android-шлюза)"
echo "  COM1:   /dev/ttyV0"
echo "============================================="
echo ""

# Удерживаем контейнер в рабочем состоянии
tail -f /dev/null
